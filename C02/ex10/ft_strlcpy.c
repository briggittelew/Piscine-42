unsigned int	ft_strlcpy(char *dest, char *src, unsigned int size)
{
	unsigned int	i;
	unsigned int	s_size;

	i = 0;
	s_size = 0;
	while (src[s_size])
		s_size++;
	if (size < 1)
		return (s_size);
	while (src[i] != '\0' && i < size - 1)
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (s_size);
}
